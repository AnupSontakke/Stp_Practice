package wrapper_class;

public class Wrapper_class {
public static void main(String[] args) {
	

	
// Type 1:- convert datatype to Wrapper class
	
	//for float
	
	float a=122f;
	float anup=Float.valueOf(a);
	System.out.println(anup);
	
	
	
	//for int
	
	int b=100;
	int an=Integer.valueOf(b);
	System.out.println(an);
	
	
	//for char
	
	char c='@';
	char gm= Character.valueOf(c);
	System.out.println(gm);
	

}
}